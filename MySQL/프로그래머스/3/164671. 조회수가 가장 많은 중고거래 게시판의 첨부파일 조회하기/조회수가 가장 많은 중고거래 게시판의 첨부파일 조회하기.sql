SELECT  
    CONCAT('/home/grep/src/', F.BOARD_ID,'/', F.FILE_ID, F.FILE_NAME, F.FILE_EXT) 
    as FILE_PATH
FROM 
    USED_GOODS_BOARD B, USED_GOODS_FILE F
WHERE 
    B.BOARD_ID = F.BOARD_ID
    AND B.VIEWS >= ALL (SELECT VIEWS
                        FROM USED_GOODS_BOARD)
ORDER BY 
    FILE_ID DESC;
WITH A AS (
    SELECT 
        FISH_TYPE, MAX(LENGTH) AS MAX
    FROM 
        FISH_INFO  
    GROUP BY
        FISH_TYPE
), 
B AS (
    SELECT 
        A.FISH_TYPE, A.MAX, N.FISH_NAME
    FROM 
        A
    JOIN 
        FISH_NAME_INFO N
    ON 
        A.FISH_TYPE = N.FISH_TYPE
)


SELECT 
    I.ID, B.FISH_NAME, I.LENGTH
FROM 
    FISH_INFO I
JOIN
    B
ON 
    I.FISH_TYPE = B.FISH_TYPE
WHERE 
    I.LENGTH = B.MAX
ORDER BY
    I.ID;